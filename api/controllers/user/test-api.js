async function mergeTranslations() {
  let translations_user = await Waspha_translation_user.find();
  let translations_driver = await Waspha_translation_driver.find();
  count = 0;
  for (t of translations_user) {
    let found = await Waspha_translation_all.find({
      where: { key: t.key },
    }).limit(1);

    if (!found.length) {
      await Waspha_translation_all.create({ key: t.key, en: t.en, ar: t.ar });
    } else {
      console.log({ found: found[0].id });
      count++;
    }
  }
  for (t of translations_driver) {
    let found = await Waspha_translation_all.find({
      where: { key: t.key },
    }).limit(1);

    if (!found.length) {
      await Waspha_translation_all.create({ key: t.key, en: t.en, ar: t.ar });
    } else {
      console.log({ found: found[0].id });
      count++;
    }
  }
  sails.log({ count: count });
}

