async function changeCharacterSet() {
  let query = `
  SELECT CONCAT( 'alter table ', a.table_name, '  CONVERT TO CHARACTER SET utf8 COLLATE utf8_general_ci;' ) AS q
  FROM information_schema.tables a
  WHERE a.table_schema = 'db_sails_prod'
  `;
  let result = await sails.sendNativeQuery(query);
  if (result.rows.length) {
    for (row in result.rows) {
      r = await sails.getDatastore().sendNativeQuery(row.q);
      console.log({ affectedRowsForExpireRFPs: r.affectedRows });
    }
  }
}
